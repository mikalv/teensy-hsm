typedef struct
{
    uint32_t key_handle;
    const char *key;
    const char *nonce;
    const char *plaintext;
    const char *ciphertext;
} value_hex_t;

typedef struct
{
    uint32_t key_handle;
    uint8_t key[AES_KEY_SIZE_BYTES];
    uint8_t nonce[AES_CCM_NONCE_SIZE_BYTES];
    uint8_t plaintext[AES_BLOCK_SIZE_BYTES];
    uint8_t ciphertext[AES_BLOCK_SIZE_BYTES + AES_CCM_MAC_SIZE_BYTES];
} value_bin_t;

static void hexdump(char *buffer, const uint8_t *values, size_t length)
{
    for (int i = 0; i < length; i++, buffer += 2)
    {
        sprintf(buffer, "%02x", values[i]);
    }
}

static void decode(uint8_t *buffer, const char *values, size_t length)
{
    for (int i = 0; i < length; i++, values += 2)
    {
        sscanf(values, "%2hhx", &buffer[i]);
    }
}

static bool encrypt_equals(const uint8_t *p_input, const uint8_t *p_key, const uint32_t key_handle, const uint8_t *p_nonce, const uint8_t *p_expected)
{
    aes_state_t key, plaintext, ciphertext;
    aes_ccm_nonce_t nonce;
    aes_ccm_mac_t mac;
    uint8_t actual[AES_BLOCK_SIZE_BYTES + AES_CCM_MAC_SIZE_BYTES];

    memcpy(plaintext.bytes, p_input, sizeof(plaintext.bytes));
    memcpy(key.bytes, p_key, sizeof(key.bytes));
    memcpy(nonce.bytes, p_nonce, sizeof(nonce.bytes));
    memset(ciphertext.bytes, 0, sizeof(ciphertext.bytes));

    AESCCM aes = AESCCM();
    aes.init(key, key_handle, nonce, AES_BLOCK_SIZE_BYTES);
    aes.encrypt_update(ciphertext, plaintext);
    aes.encrypt_final(mac);

    uint8_t *p = actual;
    memcpy(p, ciphertext.bytes, sizeof(ciphertext.bytes));
    p += sizeof(ciphertext.bytes);
    memcpy(p, mac.bytes, sizeof(mac.bytes));

#ifdef DEBUG_AES
    char str_plaintext[64];
    char str_ciphertext[64];
    char str_nonce[64];
    char str_key[64];

    hexdump(str_plaintext, plaintext.bytes, sizeof(plaintext.bytes));
    hexdump(str_ciphertext, ciphertext.bytes, sizeof(ciphertext.bytes));
    hexdump(str_nonce, nonce.bytes, sizeof(nonce.bytes));
    hexdump(str_key, key.bytes, sizeof(key.bytes));

    printf("{0x%08x, \"%s\", \"%s\", \"%s\", \"%s\"},\n", key_handle, str_key, str_nonce, str_plaintext, str_ciphertext);
#endif

    return memcmp(actual, p_expected, sizeof(actual)) == 0;
}

static bool decrypt_equals(const uint8_t *p_input, const uint8_t *p_key, const uint32_t key_handle, const uint8_t *p_nonce, const uint8_t *expected)
{
    aes_state_t key, plaintext, ciphertext;
    aes_ccm_nonce_t nonce;
    aes_ccm_mac_t mac;

    memcpy(ciphertext.bytes, p_input, sizeof(ciphertext.bytes));
    p_input += sizeof(ciphertext.bytes);
    memcpy(mac.bytes, p_input, sizeof(mac.bytes));
    memcpy(key.bytes, p_key, sizeof(key.bytes));
    memcpy(nonce.bytes, p_nonce, sizeof(nonce.bytes));
    memset(plaintext.bytes, 0, sizeof(plaintext));

    AESCCM aes = AESCCM();
    aes.init(key, key_handle, nonce, AES_BLOCK_SIZE_BYTES);
    aes.decrypt_update(plaintext, ciphertext, AES_BLOCK_SIZE_BYTES);
    bool mac_matched = aes.decrypt_final(mac);
    return mac_matched && memcmp(plaintext.bytes, expected, sizeof(plaintext.bytes)) == 0;
}

static void test_encrypt(const value_bin_t &value)
{
    char str_key[64];
    char str_nonce[64];
    char str_plaintext[64];
    char str_ciphertext[64];

    hexdump(str_key, value.key, sizeof(value.key));
    hexdump(str_nonce, value.nonce, sizeof(value.nonce));
    hexdump(str_plaintext, value.plaintext, sizeof(value.plaintext));
    hexdump(str_ciphertext, value.ciphertext, sizeof(value.ciphertext));

    bool passed = encrypt_equals(value.plaintext, value.key, value.key_handle, value.nonce, value.ciphertext);
    printf("TEST encrypt_ccm(%s,%s,0x%04x,%s) -> %s [%s]\n", str_plaintext, str_key, value.key_handle, str_nonce, str_ciphertext, passed ? "PASSED" : "FAILED");
    if (!passed)
    {
        exit(1);
    }
}

static void test_decrypt(const value_bin_t &value)
{
    char str_key[64];
    char str_nonce[64];
    char str_plaintext[64];
    char str_ciphertext[64];

    hexdump(str_key, value.key, sizeof(value.key));
    hexdump(str_nonce, value.nonce, sizeof(value.nonce));
    hexdump(str_plaintext, value.plaintext, sizeof(value.plaintext));
    hexdump(str_ciphertext, value.ciphertext, sizeof(value.ciphertext));

    bool passed = decrypt_equals(value.ciphertext, value.key, value.key_handle, value.nonce, value.plaintext);
    printf("TEST decrypt_ccm(%s,%s,0x%04x,%s) -> %s [%s]\n", str_ciphertext, str_key, value.key_handle, str_nonce, str_plaintext, passed ? "PASSED" : "FAILED");
    if (!passed)
    {
        exit(1);
    }
}

